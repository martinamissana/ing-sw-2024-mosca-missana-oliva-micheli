package it.polimi.ingsw.model.goal;

/**
 * Enumeration that indicates all possible orientations of the configuration in L_shape goals.<br>
 * Each value's name indicates in which of the four possible corners the third card needs to be placed on.
 */
public enum L_ShapeGoalType {
    UP_LEFT,
    UP_RIGHT,
    DOWN_LEFT,
    DOWN_RIGHT
}
